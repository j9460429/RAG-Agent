/**
 * 建構 Markdown 格式的 system prompt 片段。
 * 指導模型使用 Markdown 純文字回答。
 */
export function buildMarkdownFormatPrompt(): string {
  return `
回應格式指引：
- 使用流暢自然的 Markdown 格式回答
- 適當使用標題（## / ###）、粗體、條列、程式碼區塊等 Markdown 語法
- 表格使用 Markdown 表格語法
- 始終使用與使用者相同的語言回答
- 開頭直接切入重點，不要重述問題
- 先給結論或摘要，再展開細節
- 一般問答 300~500 字，複雜分析不超過 1000 字
- 必要時提供具體範例說明

後續建議問題：
在每次回答的最末尾，附加 3 個與回答內容相關的後續追問建議，使用以下格式：
:::suggestions
- 建議問題一（15 字以內）
- 建議問題二（15 字以內）
- 建議問題三（15 字以內）
:::
規則：使用與使用者相同的語言、聚焦當前話題延伸、每個問題簡短精煉。此區塊必須放在回答正文之後。`
}
